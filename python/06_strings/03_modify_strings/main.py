# UPPER CASE
a = "Hello, World!"
print(a.upper())

# LOWER CASE
a = "Hello, World!"
print(a.upper())

# REMOVE WHITESPACE
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

# REPLACE STRING
a = "Hello, World!"
print(a.replace("H", "J"))

# SPLIT STRING 
a = "Hello, World!"
print(a.split(",")) # returns ['Hello', ' World!']